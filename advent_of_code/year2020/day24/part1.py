from collections import defaultdict

from .shared import move, parse_paths


def calculate(text: str) -> int:
    paths = tuple(parse_paths(text))
    tiles = defaultdict(bool)
    for path in paths:
        pos = move((0, 0), path)
        tiles[pos] ^= True
    return sum(1 for v in tiles.values() if v)


